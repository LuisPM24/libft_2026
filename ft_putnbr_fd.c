#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	chr;

	if (n == -2147483648)
		return (ft_putstr_fd("-2147483648", fd));
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	chr = (n % 10) + '0';
	ft_putchar_fd(chr, fd);
}
